def addition(num1, num2)
  add = num1 + num2
  add
end

def subtraction(num1, num2)
  subt = num1 - num2
  subt 
end

def division(num1, num2)
  divs = num1 / num2
  divs
end

def multiplication(num1, num2)
  mult = num1 * num2
  mult 
end

def modulo(num1, num2)
  modu = num1 % num2
  modu 
end

def square_root(num)
  sq = Math.sqrt(num)
  sq 
end
